package club.code2create;

        public class Vec3 {
            private int x;
            private int y;
            private int z;

            public Vec3(int x, int y, int z) {
                this.x = x;
                this.y = y;
                this.z = z;
            }

            public int getX() {
                return x;
            }

            public int getY() {
                return y;
            }

            public int getZ() {
                return z;
            }
        }